    private void pesanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pesanActionPerformed
        String nama,jeniskopi;
        int hargakopi,pesanan,jumlahharga;
        nama = ePelanggan.getText();
        jeniskopi = (String) eKopi.getSelectedItem();
        hargakopi = Integer.parseInt(eHarga.getText());
        pesanan = Integer.parseInt(ePesanan.getText());
        jumlahharga = Integer.parseInt(eJumharga.getText());
   
        JOptionPane.showMessageDialog(this, "NAMA PELANGGAN: " + nama 
            + "\nJENIS KOPI: " + jeniskopi  
            + "\nHARGA KOPI: " + hargakopi 
            + "\nJUMLAH PESANAN: " + pesanan  
            + "\nJUMLAH HARGA: " + jumlahharga 
        );
    }//GEN-LAST:event_pesanActionPerformed

