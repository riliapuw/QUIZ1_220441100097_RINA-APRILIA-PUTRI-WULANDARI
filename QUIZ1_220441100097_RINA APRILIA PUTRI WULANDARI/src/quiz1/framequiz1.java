    private void eKopiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eKopiActionPerformed
        String jenis_kopi = (String)eKopi.getSelectedItem();
        if (jenis_kopi=="PILIH JENIS KOPI"){
        }else if(jenis_kopi=="KOPI ARABICA"){
            harga_kopi=15000;
        }else if(jenis_kopi=="KOPI ROBUSTA"){
            harga_kopi=20000;
        }else if(jenis_kopi=="KOPI LIBERIKA"){
            harga_kopi=35000;
        }else if(jenis_kopi=="KOPI LUWAK"){
            harga_kopi=30000;
        }else if(jenis_kopi=="KOPI TUBRUK"){
            harga_kopi=25000;
        }else if(jenis_kopi=="KOPI LATTE"){
            harga_kopi=40000;
        }else if(jenis_kopi=="KOPI AMERICANO"){
            harga_kopi=45000;
        }
        
        eHarga.setText(""+ harga_kopi);
    }//GEN-LAST:event_eKopiActionPerformed

    private void hitungpesananActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungpesananActionPerformed
        eharga = Integer.parseInt(eHarga.getText());
        epesanan = Integer.parseInt(ePesanan.getText());
        
        ebayar = eharga*epesanan;
        eJumharga.setText(""+ebayar);
    }//GEN-LAST:event_hitungpesananActionPerformed

